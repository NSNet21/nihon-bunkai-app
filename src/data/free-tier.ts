/**
 * Free tier data loader — Model C-refined, per-pack split.
 *
 * Bundle-split for cold-start perf (2026-05-28):
 *   • `free-tier-meta.json`   — sync import (decks list, ~30 KB).
 *                                Browse / Group Picker / Settings need
 *                                the deck list immediately, can't wait.
 *   • `free-tier-entries-{level}.json` — dynamic import, cached per
 *                                level. Memorize / Quiz / Search load
 *                                only the level they need (Search loads
 *                                all eventually via `loadAllFreeEntries`).
 *
 * JSON files generated by `scripts/extract-free-data.js` — do not edit
 * by hand. Levels currently shipped: `n5`, `glossary` (per the active
 * free tier). Future levels just add another file + bundle key here.
 */

import metaJson from './free-tier-meta.json';

import type { CsvRow, Deck, Entry } from './types';

type Meta = { decks: Omit<Deck, 'isFree' | 'source'>[] };
const meta = metaJson as unknown as Meta;

/** Lowercase level key (or `glossary`) used as the bundle filename. */
type BundleKey = 'n5' | 'n4' | 'n3' | 'n2' | 'n1' | 'glossary';

function bundleKeyForDeck(deck: Pick<Deck, 'level' | 'type'>): BundleKey {
  if (deck.type === 'glossary' || !deck.level) return 'glossary';
  return deck.level.toLowerCase() as BundleKey;
}

/** Free decks list, sync. Flagged `isFree=true` to match the type. */
const freeDecks: Deck[] = meta.decks.map((d) => ({ ...d, isFree: true, source: 'free' }));
export const decks: Deck[] = freeDecks;

/** Cache: bundle key → promise resolving to that level's entry map.
 *  Persistent for the app's lifetime so repeated Memorize / Quiz
 *  visits skip the import + parse cost. */
const entryBundleCache = new Map<BundleKey, Promise<Record<string, CsvRow[]>>>();

function loadBundle(key: BundleKey): Promise<Record<string, CsvRow[]>> {
  const cached = entryBundleCache.get(key);
  if (cached) return cached;
  /* Per-key import — Metro/webpack split each into its own chunk so
     Browse never downloads them. The literal switch is required for
     Metro's static-analysis-based chunk discovery; a computed
     `import('./free-tier-entries-' + key + '.json')` works at runtime
     but doesn't get split predictably. */
  let promise: Promise<Record<string, CsvRow[]>>;
  switch (key) {
    case 'n5':
      promise = import('./free-tier-entries-n5.json').then((m) => m.default as Record<string, CsvRow[]>);
      break;
    case 'glossary':
      promise = import('./free-tier-entries-glossary.json').then((m) => m.default as Record<string, CsvRow[]>);
      break;
    default:
      /* Future levels (n4/n3/n2/n1) — add their `import` branch when
         the corresponding entries-{level}.json starts shipping. */
      promise = Promise.resolve({});
  }
  entryBundleCache.set(key, promise);
  return promise;
}

/** Lookup the level bundle for a deck and read its entry rows.
 *  Returns `[]` if the deck or its bundle is missing. */
export async function entriesForDeckAsync(deckId: string): Promise<Entry[]> {
  const deck = freeDecks.find((d) => d.id === deckId);
  if (!deck) return [];
  const bundle = await loadBundle(bundleKeyForDeck(deck));
  const rows = bundle[deckId];
  if (!rows) return [];
  return rows.map((r) => ({
    id: `${deck.pack}-${r.no}`,
    type: deck.type,
    level: deck.level,
    pack: deck.pack,
    tags: deck.tags,
    no: r.no,
    t: r.t,
    d: r.d,
    p: r.p,
    e: r.e,
  }));
}

/** Eagerly load every free entry — used by Search to build its Fuse
 *  index. Triggers parallel imports of every shipped level bundle. */
export async function loadAllFreeEntries(): Promise<Entry[]> {
  /* Derive needed bundle keys from the actual decks list so adding a
     new level only requires regenerating the JSON, not touching this. */
  const keys = new Set<BundleKey>();
  for (const d of freeDecks) keys.add(bundleKeyForDeck(d));
  const bundles = await Promise.all([...keys].map(loadBundle));
  const out: Entry[] = [];
  for (const deck of freeDecks) {
    /* Find the bundle that contains this deck by checking each loaded
       map — small N (≤6 keys), constant cost. */
    for (const bundle of bundles) {
      const rows = bundle[deck.id];
      if (!rows) continue;
      for (const r of rows) {
        out.push({
          id: `${deck.pack}-${r.no}`,
          type: deck.type,
          level: deck.level,
          pack: deck.pack,
          tags: deck.tags,
          no: r.no,
          t: r.t,
          d: r.d,
          p: r.p,
          e: r.e,
        });
      }
      break;
    }
  }
  return out;
}

export const TOTAL_FREE_ENTRIES = freeDecks.reduce((s, d) => s + d.entryCount, 0);
